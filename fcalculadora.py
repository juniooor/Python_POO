class Calculadora:

    def __init__(self):
       pass
        
    def soma(self, numa, numb):
        return numa + numb 
    
    def subtracao(self, numa, numb):
        return numa - numb 

    def divisao(self, numa, numb):
        return numa / numb 

    def multiplicacao(self, numa, numb):
        return numa * numb        